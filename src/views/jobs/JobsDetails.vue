<template>
	<div v-if="job">
		<h1>{{ job.title}}</h1>
		<!-- <p>the job id is {{ $route.params.id}}</p> -->
		<p>the job id is {{ id }}</p>
    <p>{{ job.details }}</p>
	</div>
  <div v-else>
    Loading Job...
  </div> 
</template>

<script>
export default {
	name: "JobsDetail",
  props: ["id"],
  data() {
    return {
      job: null
    }
  },
  mounted() {
    fetch('http://localhost:3000/jobs/' + this.id)
      .then(res => res.json())
      .then(data => this.job = data)
      .catch(err => console.log(err.message))
  }
};
</script>

